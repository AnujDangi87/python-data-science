name = "anuj"

#Comparison operators
if(name == "anuj"):
    print("yes")
else:
    print("no")

age = 22

#Branching
if(age > 18):
    print("you can enter")
elif(age == 18):
    print("go see admin")
else:
    print("you can not enter")

#Logic operators (or,and, not)
if(name == "anuj" or age == 23):
    print("correct")

if(name != "anu" and age != 23):
    print("correct")